import java.io.FileInputStream;
import java.io.FileOutputStream;
import java.io.IOException;
import java.io.ObjectInputStream;
import java.io.ObjectOutputStream;
import java.io.Serializable;

/**
 * description
 *
 * @author 程绍壮
 * @datetime 2020-07-16 17:20
 */
public class SerializableTests {

    public static void main(String[] args) throws IOException, ClassNotFoundException {

        Person personOut = new Person(123, "abc");
        String objectFile = "C:\\Users\\chengsean\\Desktop\\hello";

        ObjectOutputStream objectOutputStream = new ObjectOutputStream(new FileOutputStream(objectFile));
        objectOutputStream.writeObject(personOut);
        objectOutputStream.close();

        ObjectInputStream objectInputStream = new ObjectInputStream(new FileInputStream(objectFile));
        Person personIn = (Person) objectInputStream.readObject();
        objectInputStream.close();
        System.out.println(personIn);
    }

    private static class Person implements Serializable {

        private int age;
        private String name;

        Person(int age, String name) {
            this.age = age;
            this.name = name;
        }

        @Override
        public String toString() {
            return "age = " + age + "  " + "name = " + name;
        }
    }
}
